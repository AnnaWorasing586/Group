#include <iostream>
using namespace std;

int main() {

  	// for loop to print "Hi" 5 times
    for (int i = 0; i < 10; i++) { //สร้างตัวแปร i เริ่มต้นที่ 0(ลูปจะทำงานตราบใดที่ i น้อยกว่า 10) เพิ่มค่า i ทีละ 1 หลังจากจบรอบของลูป

      	cout << "Hi => i = " << i << endl; //พิมพ์ค่าของตัวแปร i และ ขึ้นบรรทัดใหม่หลังจากแสดงข้อความ
    }
  
    return 0; //ส่งค่ากลับ 0 เพื่อบอกว่าฟังก์ชัน main() ทำงานเสร็จสมบูรณ์โดยไม่มีข้อผิดพลาด
}